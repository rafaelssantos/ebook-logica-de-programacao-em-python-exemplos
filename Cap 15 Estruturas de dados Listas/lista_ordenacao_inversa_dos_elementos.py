lista1 = [3, 1, 4, 2]
lista1.sort(reverse=True)   # Ordena em ordem decrescente
print(lista1)               # [4, 3, 2, 1]

lista2 = [3, 1, 4, 2]
lista2.sort()               # Ordena em ordem crescente
lista2.reverse()            # Inverte a ordem atual dos elementos
print(lista2)               # [4, 3, 2, 1]