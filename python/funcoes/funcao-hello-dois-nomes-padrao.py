def saudacao_personalizada2(nome1 = "Fulano", nome2 = "Beltrano"):
    print("Hello,", nome1, "e", nome2, "!")

print("Estamos aprendendo a usar funções!")
alguem1 = "Guido van Rossum"
alguem2 = "Alan Turing"
saudacao_personalizada2(alguem1, alguem2)	    # Dois argumentos
saudacao_personalizada2(alguem1)			    # Um argumento
saudacao_personalizada2()			            # Sem argumentos
print("Continuação do programa após a execução da função.")