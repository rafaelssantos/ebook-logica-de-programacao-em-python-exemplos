valor = 42
print(f"O valor é {valor}")         # O valor é 42

valor = 42
print(f"O valor é {valor:05d}")     # O valor é 00042

valor = 1000000
print(f"O valor é {valor:,}")       # O valor é 1,000,000

valor = 3.14159
print(f"O valor é {valor}")

valor = 3.14159
print(f"O valor é {valor:.2f}")     # O valor é 3.14

valor = 1234567.8910
print(f"O valor é {valor:,.2f}")    # O valor é 1,234,567.89
