def soma(a, b):
    """
    Retorna a soma de dois números.

    Parâmetros:
    a (int ou float): O primeiro número a ser somado.
    b (int ou float): O segundo número a ser somado.

    Retorno:
    int ou float: O resultado da soma de a e b.
    """
    return a + b

# ---------------------------------------------------------

resultado = soma(5, 3)
print("O resultado da soma é:", resultado)